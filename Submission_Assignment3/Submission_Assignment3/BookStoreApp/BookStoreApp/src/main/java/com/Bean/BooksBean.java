package com.Bean;

/**
 *
 * @author jaspr
 */
public class BooksBean {
    
    private String isbn;
    private String title;
    private String author;
    private Double price;
    
    public BooksBean(){
        
    }
    
    public BooksBean(String isbn, String title, String author, Double price){
        this.isbn = isbn;
        this.title = title;
        this.author = author;
        this.price = price;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    @Override
    public String toString() {
        return title;
    }

}
